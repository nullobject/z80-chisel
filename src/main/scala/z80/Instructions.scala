  def INC_D  = 0x14
  def INC_E  = 0x1c
  def INC_H  = 0x24
  def INC_L  = 0x2c
  def INC_HL = 0x34
  def INC_A  = 0x3c
  def LD_A   = 0x3e
  def HALT   = 0x76
}
